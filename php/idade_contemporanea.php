                        <h2>Idade Contemporânea</h2>
                        <p>Desde então, o mundo passou por diversas transformações nas suas bases sociais, econômicas, políticas e culturais. A revolução que ocorreu na França marcou a extinção do Antigo Regime – época em que a sociedade era estruturada de acordo com pirâmides sociais, sendo o rei, a nobreza e o clero os maiores acumuladores de riquezas.</p>
                        <p>A corrente iluminista também influenciou o desenvolvimento dessa nova era da humanidade. A razão passou a superar as convicções religiosas e a ciência usada para o descobrimento de tecnologias. A contemporaneidade fortaleceu o sistema capitalista no ocidente, criando conflitos entre as potências europeias na busca pelo domínio de terras, exploração de matérias-primas e expansão dos mercados.</p>
                        <h3>O mundo durante a Idade Contemporânea</h3>
                        <p>Cronologicamente, a Idade Contemporânea iniciou-se com a queda da Bastilha, acontecimento que iniciou a Revolução Francesa, em 1789. Esse evento é conhecido historicamente como o que garantiu a ascensão da burguesia e a consolidação do iluminismo, que pregava a primazia da razão e do estudo científico como forma de garantir-se o desenvolvimento humano. </p>
                        <p>Por meio dos ideais iluministas, o mundo presenciou, durante a Idade Contemporânea, um elevado grau de desenvolvimento científico, e a sociedade, sobretudo a ocidental, passou por um relativo processo de laicização. Com isso, as questões de Estado desvincularam-se da Igreja, e o poderio político religioso diminuiu. </p>
                        <p>O desenvolvimento científico da Idade Contemporânea garantiu a continuidade da Revolução Industrial e do processo de consolidação do capitalismo. Novas tecnologias surgiram, o modo de produção tornou-se mais técnico, e os avanços levaram ao surgimento de itens como automóveis e antibióticos. Por outro lado, a capacidade de destruição da humanidade aumentou-se, uma vez que a tecnologia permitiu a criação de armas mais letais. </p>
                        <p>Do ponto de vista político, a Idade Contemporânea foi o período em que as monarquias absolutistas enfraqueceram-se, sendo substituídas por monarquias constitucionais, forma de governo que possui mais dispositivos democráticos, e por repúblicas, algumas democráticas, mas outras autoritárias. </p>
                        <p>Ainda do ponto de vista político, mas também entrando em questões econômicas, a Idade Contemporânea presenciou o surgimento de uma ideologia que propunha uma alternativa para a exploração praticada contra os trabalhadores no capitalismo: o socialismo. Os ideais socialistas mobilizaram trabalhadores em todo o mundo, desejosos de um mundo sem exploração. </p>
                        <p>No século XX, os ideais socialistas avançaram, e o primeiro país socialista do mundo surgiu em 1917, quando os bolcheviques realizaram um golpe e tomaram o poder na Rússia. Os ideais socialistas expandiram-se para outros países e fizeram com que o mundo se polarizasse entre nações socialistas e nações capitalistas. </p>  
