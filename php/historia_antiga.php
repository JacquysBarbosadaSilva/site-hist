                    <div class="card-Historia">
                        <h3>Grécia: </h3>
                        <p>Os gregos foram um dos grandes povos da Antiguidade e deixaram grandes contribuições na política, filosofia, matemática, história e muitas outras áreas. Essa civilização começou a desenvolver-se por meio da migração de alguns povos para o sul da Península Balcânica, a partir de 2000 a.C. </p>
                        <p>Os gregos surgiram da fusão de povos, como: </p>
                        <ul class="lista-espaço">
                            <li>Cretenses. </li>
                            <li>Micênicos. </li>
                            <li>Eólios. </li>
                            <li>Dórios. </li>
                            <li>Jônios. </li>
                        </ul>
                        <h3>A história grega foi dividida em uma série de períodos: </h3>
                        <h4>Pré-Homérico: </h4>
                        <p> É marcado pela existência de duas grandes civilizações desenvolvidas por cretenses e micênicos.</p>
                        <p>Os gregos, durante o seu Período Clássico, ficaram conhecidos por terem desenvolvido a pólis, um modelo de cidade-estado, tendo em Atenas e Esparta os exemplos mais marcantes. Essas foram as maiores e mais poderosas pólis da Grécia Antiga, possuindo vastas terras e rivalizando entre si, já que possuíam diferentes modelos de pólis e diferentes interesses. </p>                
                    <h4>Atenienses: </h4>
                        <p>Possuíam um modelo democrático, que permitia a participação de todos os cidadãos da sociedade (homens, nascidos em Atenas e filhos de atenienses), enquanto os espartanos adotaram o modelo aristocrático, que permitia a participação de uma diminuta minoria de privilegiados, conhecidos como esparciatas. </p>
                        <h3>A história grega ficou marcada por dois grandes conflitos: </h3>
                        <ul class="lista-espaço">
                            <li>Guerras Médicas: contra os persas; </li>
                            <li>Guerra do Peloponeso: guerra civil entre atenienses e espartanos. </li>
                        </ul>
                        <p>As duas guerras enfraqueceram as pólis e permitiram que estrangeiros, como os macedônicos, conquistassem a região. </p>
                        <h3> Roma Antiga: </h3>
                        <p>A civilização romana surgiu a partir de uma pequena cidade latina que se desenvolveu na Península Itálica, no século VIII a.C. Roma foi uma das grandes civilizações da Antiguidade e possuiu um território de grandes dimensões que se estendia da Mesopotâmia à Europa Ocidental e da Bretanha até o norte da África. </p>
                        <p>A história romana foi dividida em três grandes períodos que são: </p>
                        <ul class="lista-espaço">
                            <li>o monárquico; </li>
                            <li>o republicano; </li>
                            <li>o imperial. </li>
                        </ul>
                        <p>Da pequena aldeia na região do Lácio, Roma tornou-se uma gigantesca civilização que se expandiu por meio de guerras. As marcas da presença romana, nos locais que eles conquistaram, resistiram por um longo prazo e muitas existem até os dias atuais. </p>
                        <p>A sociedade romana era bem dividida entre patrícios e plebeus e, ao longo da história, as desigualdades sociais levaram essas classes a entrarem em choque por diversas vezes. Uma das grandes demonstrações dos embates entre plebeus e patrícios pode ser encontradas nas reformas propostas pelos irmãos Graco. </p>
                        <p>A decadência romana foi iniciada a partir do século III d.C. e teve relação direta com o enfraquecimento da economia romana. Primeiramente, o poderio de Roma sobre suas províncias enfraqueceu, e a economia romana esfacelou-se, principalmente pela crise do sistema de escravos, que sustentava a produção romana. </p>
                        <p>Além disso, o controle romano sobre suas fronteiras começou a declinar, e os povos germânicos e outros bárbaros do limes começaram a penetrar no território romano. Muitos foram agregados ao exército romano e, em troca, recebiam terras no território. As invasões germânicas foram o evento que selou o fim do Império Romano na Europa Ocidental. </p>
                        <p>Em 476 d.C., o imperador Rômulo Augusto foi deposto pelos hérulos, e os territórios do Império Romano do Ocidente foram ocupados por diferentes povos germânicos. A Idade Média estabeleceu-se dessa fusão entre a cultura latina com a cultura germânica. </p>
