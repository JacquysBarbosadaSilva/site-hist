                        <h2>História antiga</h2>
                        <img src="hist-primitiva.webp" alt="">
                        <p>A História Antiga é uma época histórica marcada pelo surgimento e desenvolvimento das primeiras civilizações (civilizações antigas). Esse período teve início há 4.000 a.C, com o surgimento da escrita (surgimento da escrita cuneiforme), e terminou em 476, com o declínio do Império Romano do Ocidente. </p>
                        <p>A Idade Antiga estendeu-se até o ano de 476 d.C., quando houve a desagregação do Império Romano do Ocidente. Esse acontecimento marcou o fim do Império Romano na Europa Ocidental e deu início ao processo de ocupação desse continente pelos reinos germânicos. Isso permitiu a fusão entre a cultura latina e a cultura germânica e deu forma à Idade Média. </p>
                        <p>Quando falamos de civilizações orientais, estamos considerando os povos mesopotâmicos, fenícios, hebreus, persas, egípcios, hititas e muitos outros. No caso das civilizações clássicas, estamos nos referindo aos gregos e aos romanos. No caso dos gregos, costumam ser incluídos os cretenses e os micênicos. </p>
                        <h2>Principais civilizações da Antiguidade: </h2>
                        <h3> Egito Antigo: </h3>
                        <p>A civilização egípcia desenvolveu-se no nordeste do continente africano, às margens do Rio Nilo, cujas cheias proporcionavam à região um solo bastante fértil. A existência do Nilo garantia a possibilidade de sobrevivência humana em um local extremamente hostil: o deserto do Saara. </p>
                        <p>Ao longo das margens do Rio Nilo, uma série de comunidades, chamadas de nomos, desenvolveram-se. Por volta de 3500 a.C., essas comunidades formaram dois reinos conhecidos como: </p>
                        <ul class="lista-espaço">
                            <li>Baixo Egito. </li>
                            <li>Alto Egito. </li>
                        </ul>
                        <p>O Egito possuía uma monarquia teocrática e, assim, a religião tinha importância crucial na execução do poder político. O governante, chamado de faraó, era considerado a manifestação de um deus e tinha poderes plenos sobre as terras egípcias. Essa civilização ficou conhecida: </p>
                        <ul class="lista-espaço">
                            <li>Pela escrita por meio de hieróglifos. </li>
                            <li>Pela construção de grandes túmulos – as pirâmides. </li>
                            <li>Pela prática de mumificar os mortos.</li>
                        </ul>    
                        <h3>Mesopotâmia: </h3>
                        <p>A Mesopotâmia não representa uma civilização, mas uma região do Oriente Médio que abrigou uma série de povos da Antiguidade. O termo “mesopotâmia” tem origem grega e significa “terra entre rios”, uma menção ao fato de que as civilizações mesopotâmicas se estabeleceram nessa região localizada entre dois rios: Tigre e Eufrates. </p>
                        <p>Entre as principais civilizações mesopotâmicas destacam-se: </p>
                        <ul class="lista-espaço">
                            <li>Sumérios. </li>
                            <li>Acádios. </li>
                            <li>Amoritas.</li>
                            <li>Assírios.</li>
                            <li>Caldeus. </li>
                        </ul>
                        <p>Desses, os sumérios foram os primeiros a ganhar notoriedade, formando uma civilização mais avançada por volta de 3200 a.C. Eles desenvolveram as primeiras cidades, criaram a escrita cuneiforme, estabeleceram governos locais e fizeram grandes construções. </p>
                        <p>Outras grandes civilizações, como os amoritas, ficaram marcadas por um grande rei chamado Hamurábi. Esse rei governava um pequeno império a partir da Babilônia e ficou conhecido pelo Código de Hamurábi, conjunto de leis escritas que tiveram grande influência na Mesopotâmia. Os assírios ficaram conhecidos por sua grande violência, e os caldeus tiveram o último grande império mesopotâmico. Em 539 a.C., a região foi conquistada pelos persas. </p>
